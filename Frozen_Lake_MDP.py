# 1. Basic MDP definition
# =========================

import numpy as np

GRID_SIZE = 4
N_STATES = 16

LEFT = 0
DOWN = 1
RIGHT = 2
UP = 3

ACTIONS = [LEFT, DOWN, RIGHT, UP]

ACTION_NAMES = {
    LEFT: "Left",
    DOWN: "Down",
    RIGHT: "Right",
    UP: "Up"
}

ACTION_SYMBOLS = {
    LEFT: "L",
    DOWN: "D",
    RIGHT: "R",
    UP: "U"
}

START_STATE = 0
GOAL_STATE = 15
HOLES = {5, 7, 12}
TERMINAL_STATES = HOLES | {GOAL_STATE}

GAMMA = 0.9

# Assignment-specific transition behavior:
# chosen action succeeds with probability 0.4
# each perpendicular direction occurs with probability 0.3
ACTION_OUTCOMES = {
    LEFT:  [(0.4, LEFT),  (0.3, UP),    (0.3, DOWN)],
    DOWN:  [(0.4, DOWN),  (0.3, LEFT),  (0.3, RIGHT)],
    RIGHT: [(0.4, RIGHT), (0.3, UP),    (0.3, DOWN)],
    UP:    [(0.4, UP),    (0.3, LEFT),  (0.3, RIGHT)],
}

# ============================================================
# Helper functions for grid movement
# ============================================================

def state_to_rowcol(state):
    """Convert a state number into (row, col)."""
    row = state // GRID_SIZE
    col = state % GRID_SIZE
    return row, col

def rowcol_to_state(row, col):
    """Convert (row, col) into a state number."""
    return row * GRID_SIZE + col

def move(state, action):
    """
    Apply an action in the grid.
    If the move would leave the grid, the agent stays in place.
    """
    row, col = state_to_rowcol(state)

    if action == LEFT:
        new_row, new_col = row, col - 1
    elif action == DOWN:
        new_row, new_col = row + 1, col
    elif action == RIGHT:
        new_row, new_col = row, col + 1
    elif action == UP:
        new_row, new_col = row - 1, col
    else:
        raise ValueError(f"Invalid action: {action}")

    # Boundary rule: if outside the grid, stay in the same state
    if not (0 <= new_row < GRID_SIZE and 0 <= new_col < GRID_SIZE):
        return state

    return rowcol_to_state(new_row, new_col)

def reward_function(state, action, next_state):
    """
    Reward function R(s, a, s'):
    +1 only when entering the goal state.
    0 otherwise.
    """
    return 1.0 if next_state == GOAL_STATE else 0.0

# ============================================================
# Part 1: Build the MDP transition dictionary
# ============================================================

def build_mdp():
    """
    Build transition dictionary P where:
    P[s][a] = list of (prob, next_state, reward, done)

    done = True if next_state is a terminal state
    """
    P = {s: {a: [] for a in ACTIONS} for s in range(N_STATES)}

    for s in range(N_STATES):
        for a in ACTIONS:

            # Terminal states loop to themselves
            if s in TERMINAL_STATES:
                P[s][a] = [(1.0, s, 0.0, True)]
                continue

            transitions = []

            for prob, actual_action in ACTION_OUTCOMES[a]:
                next_state = move(s, actual_action)
                reward = reward_function(s, a, next_state)
                done = next_state in TERMINAL_STATES
                transitions.append((prob, next_state, reward, done))

            P[s][a] = transitions

    return P

P = build_mdp()

