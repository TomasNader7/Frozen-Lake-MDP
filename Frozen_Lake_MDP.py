def policy_evaluation(policy, P, gamma=0.9, num_iterations=5):
    """
    Perform a fixed number of policy-evaluation iterations.
    Returns:
        V       - value function after num_iterations
        history - list of V after each iteration, including iteration 0
    """
    V = np.zeros(N_STATES)
    history = [V.copy()]

    for _ in range(num_iterations):
        new_V = np.zeros(N_STATES)

        for s in range(N_STATES):
            if s in TERMINAL_STATES:
                continue

            a = policy[s]
            for prob, next_state, reward, done in P[s][a]:
                future = 0.0 if done else gamma * V[next_state]
                new_V[s] += prob * (reward + future)

        V = new_V
        history.append(V.copy())

    return V, history

def policy_evaluation_until_convergence(policy, P, gamma=0.9, theta=0.001):
    """
    Evaluate a fixed policy until convergence.
    Convergence criterion:
        max_s |V_new(s) - V_old(s)| < theta
    """
    V = np.zeros(N_STATES)
    history = [V.copy()]
    iterations = 0

    while True:
        iterations += 1
        delta = 0.0
        new_V = np.zeros(N_STATES)

        for s in range(N_STATES):
            if s in TERMINAL_STATES:
                continue

            a = policy[s]
            for prob, next_state, reward, done in P[s][a]:
                future = 0.0 if done else gamma * V[next_state]
                new_V[s] += prob * (reward + future)

            delta = max(delta, abs(new_V[s] - V[s]))

        V = new_V
        history.append(V.copy())

        if delta < theta:
            break

    return V, history, iterations

