def value_iteration(P, gamma=0.9, theta=0.001):
    """
    Run value iteration until convergence:
        max_s |V_new(s) - V_old(s)| < theta
    Returns:
        V_opt
        optimal_policy
        num_iterations
    """
    V = np.zeros(N_STATES)
    iterations = 0

    while True:
        iterations += 1
        delta = 0.0
        new_V = V.copy()

        for s in range(N_STATES):
            if s in TERMINAL_STATES:
                continue

            action_values = []

            for a in ACTIONS:
                q_sa = 0.0
                for prob, next_state, reward, done in P[s][a]:
                    future = 0.0 if done else gamma * V[next_state]
                    q_sa += prob * (reward + future)
                action_values.append(q_sa)

            new_V[s] = max(action_values)
            delta = max(delta, abs(new_V[s] - V[s]))

        V = new_V

        if delta < theta:
            break

    # Extract optimal policy from converged V
    optimal_policy = {}
    for s in range(N_STATES):
        if s in TERMINAL_STATES:
            continue

        q_values = []
        for a in ACTIONS:
            q_sa = 0.0
            for prob, next_state, reward, done in P[s][a]:
                future = 0.0 if done else gamma * V[next_state]
                q_sa += prob * (reward + future)
            q_values.append(q_sa)

        optimal_policy[s] = int(np.argmax(q_values))

    return V, optimal_policy, iterations


