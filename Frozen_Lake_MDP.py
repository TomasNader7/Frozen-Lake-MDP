def compute_q_from_v(V, P, gamma=0.9):
    """
    Compute Q(s,a) from a value function V using one-step lookahead.
    """
    Q = np.zeros((N_STATES, len(ACTIONS)))

    for s in range(N_STATES):
        if s in TERMINAL_STATES:
            continue

        for a in ACTIONS:
            for prob, next_state, reward, done in P[s][a]:
                future = 0.0 if done else gamma * V[next_state]
                Q[s, a] += prob * (reward + future)

    return Q

def policy_improvement(V, P, gamma=0.9):
    """
    Improve a policy greedily with respect to V.
    Returns:
        improved_policy
        Q
    """
    Q = compute_q_from_v(V, P, gamma)
    improved_policy = {}

    for s in range(N_STATES):
        if s not in TERMINAL_STATES:
            improved_policy[s] = int(np.argmax(Q[s]))

    return improved_policy, Q

