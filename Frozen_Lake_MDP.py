def print_transitions_for_state(P, state):
    """Print all transitions for one state."""
    print(f"\nState {state}")
    for a in ACTIONS:
        print(f"  Action {ACTION_NAMES[a]}:")
        for prob, next_state, reward, done in P[state][a]:
            print(f"    prob={prob:.1f}, next={next_state}, reward={reward}, done={done}")

def print_value_grid(V, title="Value Function"):
    """Print a 4x4 grid of state values."""
    print(f"\n{title}")
    for r in range(GRID_SIZE):
        row_vals = []
        for c in range(GRID_SIZE):
            s = rowcol_to_state(r, c)
            row_vals.append(f"{V[s]:7.3f}")
        print(" ".join(row_vals))


def print_policy_grid(policy, title="Policy"):
    """
    Print a 4x4 grid of policy actions.
    H = hole, G = goal
    """
    print(f"\n{title}")
    for r in range(GRID_SIZE):
        row_symbols = []
        for c in range(GRID_SIZE):
            s = rowcol_to_state(r, c)

            if s in HOLES:
                row_symbols.append(" H ")
            elif s == GOAL_STATE:
                row_symbols.append(" G ")
            else:
                row_symbols.append(f" {ACTION_SYMBOLS[policy[s]]} ")
        print(" ".join(row_symbols))

def print_q_values(Q):
    """Print Q(s,a) for each non-terminal state."""
    print("\nQ-values by state")
    for s in range(N_STATES):
        if s in TERMINAL_STATES:
            continue
        print(
            f"State {s:2d}: "
            f"L={Q[s, LEFT]:.4f}  "
            f"D={Q[s, DOWN]:.4f}  "
            f"R={Q[s, RIGHT]:.4f}  "
            f"U={Q[s, UP]:.4f}"
        )


def compare_policies(old_policy, new_policy):
    """Return list of states where the policy action changed."""
    changed_states = []
    for s in old_policy:
        if old_policy[s] != new_policy[s]:
            changed_states.append(s)
    return changed_states

